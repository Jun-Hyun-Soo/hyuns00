// ***********************************************************************************************************  
// 문자열에 공백이 있는지 검사
// ***********************************************************************************************************  
function checkEmpty(strText) {
	try {
		if (strText.match(/^[ ]*$/)) {
			return true;
		} else {
			return false;
		}
	}
	catch (ex) {
		alert(ex.description);
	}
}

// ***********************************************************************************************************
// 문자열이 숫자만 존재하는지 검사
// ***********************************************************************************************************
function checkNumber(strText) {
	try {
		if (strText.match(/^[-]{0,1}[0-9.]+$/)) {
			return true;
		} else {
			return false;
		}
	}
	catch (ex) {
		alert(ex.description);
	}
}

