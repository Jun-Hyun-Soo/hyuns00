// ***********************************************************************************************************
// <br> => \r\n 으로 변경
// ***********************************************************************************************************
function replaceBR(strText) {
	var strReturn = "";

	try {
		strReturn = strText.replace(/<br>/gi, "\r\n");
	}
	catch (ex) {
		alert(ex.description);
	}

	return strReturn;
}
