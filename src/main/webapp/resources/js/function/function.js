// ***********************************************************************************************************
// 이메일이 제대로 되어 있는지 검사
// ***********************************************************************************************************
function checkEmail(strText) {
	try {
		if (strText.match(/^(([a-zA-Z0-9_-]+\.)*[a-zA-Z0-9_-]+@([a-zA-Z0-9_-]+\.)+[a-zA-Z0-9_-]+)$/)) {
			return true;
		} else {
			return false;
		}
	}
	catch (ex) {
		alert(ex.description);
	}
}

// ***********************************************************************************************************
// 파일 확장자 검사 true 이면 업로드 불가
